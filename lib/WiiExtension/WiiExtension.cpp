WiiExtension::WiiExtension(PeripheralI2C *i2cController, uint8_t addr) {
    i2c = i2cController;
    address = addr;
}

void WiiExtension::begin() {
    if (this->extensionMode) {
        i2c_slave_init(i2c->getController(), address, &this->i2cHandler);
    } else {
        doI2CInit();
#if WII_EXTENSION_DEBUG==true
        printf("WiiExtension::begin\n");
#endif
    }

    isReady = false;
}

void WiiExtension::start() {
    if (this->extensionMode) {
        memset((void *)WiiExtension_extensionMemory, 0, sizeof WiiExtension_extensionMemory);
        WiiExtension_memoryCursor = 0;
        WiiExtension_hasAddress = false;
        WiiExtension_firstRead = true;
        WiiExtension_isCryptoEnabled = false;

        // reset extension type
        WiiExtension_extensionMemory[0xFA+0] = 0x00;
        WiiExtension_extensionMemory[0xFA+1] = 0x00;
        WiiExtension_extensionMemory[0xFA+2] = 0xA4;
        WiiExtension_extensionMemory[0xFA+3] = 0x20;
        WiiExtension_extensionMemory[0xFA+4] = 0x01;
        WiiExtension_extensionMemory[0xFA+5] = 0x00;

        // change to extension ID
        switch (extensionType) {
            case WII_EXTENSION_NUNCHUCK:
            {
                WiiExtension_extensionMemory[0xFA+0] = 0x00;
                WiiExtension_extensionMemory[0xFA+4] = 0x00;
                WiiExtension_extensionMemory[0xFA+5] = 0x00;
                extensionController = new NunchuckExtension();

                // set neutral position
                WiiExtension_extensionMemory[0x00] = 0xA0;  // RX<4:3>=10, LX=0x20
                WiiExtension_extensionMemory[0x01] = 0x20;  // RX<2:1>=00, LY=0x20
                WiiExtension_extensionMemory[0x02] = 0x10;  // RX<0>=0, LT<4:3>=0, RY=0x10
                WiiExtension_extensionMemory[0x03] = 0x00;  // LT<2:0>=0, RT=0
                WiiExtension_extensionMemory[0x04] = 0xFF;  // buttons unpressed
                WiiExtension_extensionMemory[0x05] = 0xFF;

                // set default calibration
                uint8_t cal[16] = {
                    0x00, 0x00, 0x00,  // 0GX<9:2>, 0GY<9:2>, 0GZ<9:2>
                    0x00,              // LSB of 0G X, Y, Z
                    0x00, 0x00, 0x00,  // 1GX<9:2>, 1GY<9:2>, 1GZ<9:2>
                    0x00,              // LSB of 1G X, Y, Z
                    0x00, 0x00, 0x00,  // X max, min, center
                    0x00, 0x00, 0x00,  // Y
                    0x00, 0x00,        // checksum (filled below)
                };
                uint8_t checksum = 0x55;
                for (int i = 0; i < 14; i++) checksum += cal[i];
                cal[14] = checksum;
                cal[15] = (uint8_t)(checksum + 0x55);

                memcpy((void *)&WiiExtension_extensionMemory[0x20], cal, 16);
                memcpy((void *)&WiiExtension_extensionMemory[0x30], cal, 16);  // mirror
                break;
            }
            case WII_EXTENSION_CLASSIC:
            case WII_EXTENSION_CLASSIC_PRO:
            {
                WiiExtension_extensionMemory[0xFA+0] = 0x00;
                if (extensionType == WII_EXTENSION_CLASSIC) {
                    WiiExtension_extensionMemory[0xFA+4] = 0x01;
                }
                WiiExtension_extensionMemory[0xFA+5] = 0x01;
                extensionController = new ClassicExtension();

                // set neutral position
                WiiExtension_extensionMemory[0x00] = 0xA0;  // RX<4:3>=10, LX=0x20
                WiiExtension_extensionMemory[0x01] = 0x20;  // RX<2:1>=00, LY=0x20
                WiiExtension_extensionMemory[0x02] = 0x10;  // RX<0>=0, LT<4:3>=0, RY=0x10
                WiiExtension_extensionMemory[0x03] = 0x00;  // LT<2:0>=0, RT=0
                WiiExtension_extensionMemory[0x04] = 0xFF;  // buttons unpressed
                WiiExtension_extensionMemory[0x05] = 0xFF;

                // set default calibration
                uint8_t cal[16] = {
                    0x00, 0x00, 0x00,  // LX max, min, center
                    0x00, 0x00, 0x00,  // LY
                    0x00, 0x00, 0x00,  // RX
                    0x00, 0x00, 0x00,  // RY
                    0x00, 0x00,        // LT, RT neutral
                    0x00, 0x00,        // checksum (filled below)
                };
                uint8_t checksum = 0x55;
                for (int i = 0; i < 14; i++) checksum += cal[i];
                cal[14] = checksum;
                cal[15] = (uint8_t)(checksum + 0x55);

                memcpy((void *)&WiiExtension_extensionMemory[0x20], cal, 16);
                memcpy((void *)&WiiExtension_extensionMemory[0x30], cal, 16);  // mirror
                break;
            }
            case WII_EXTENSION_GUITAR:
            case WII_EXTENSION_DRUMS:
            case WII_EXTENSION_TURNTABLE:
            {
                WiiExtension_extensionMemory[0xFA+4] = 0x01;
                WiiExtension_extensionMemory[0xFA+5] = 0x03;
                if (extensionType == WII_EXTENSION_GUITAR) {
                    WiiExtension_extensionMemory[0xFA+0] = 0x00;
                    extensionController = new GuitarExtension();
                } else if (extensionType == WII_EXTENSION_DRUMS) {
                    WiiExtension_extensionMemory[0xFA+0] = 0x01;
                    extensionController = new DrumExtension();
                } else if (extensionType == WII_EXTENSION_TURNTABLE) {
                    WiiExtension_extensionMemory[0xFA+0] = 0x03;
                    extensionController = new TurntableExtension();
                }
                break;
            }
            case WII_EXTENSION_TAIKO:
                WiiExtension_extensionMemory[0xFA+0] = 0x00;
                WiiExtension_extensionMemory[0xFA+4] = 0x01;
                WiiExtension_extensionMemory[0xFA+5] = 0x11;
                extensionController = new TaikoExtension();
                break;
            case WII_EXTENSION_TRAIN:
                WiiExtension_extensionMemory[0xFA+0] = 0x00;
                WiiExtension_extensionMemory[0xFA+4] = 0x03;
                WiiExtension_extensionMemory[0xFA+5] = 0x10;
                extensionController = new TrainExtension();
                break;
            case WII_EXTENSION_DRAWSOME:
                WiiExtension_extensionMemory[0xFA+0] = 0xFF;
                WiiExtension_extensionMemory[0xFA+4] = 0x00;
                WiiExtension_extensionMemory[0xFA+5] = 0x13;
                //extensionController = new DrawsomeExtension();
                break;
            case WII_EXTENSION_UDRAW:
                WiiExtension_extensionMemory[0xFA+0] = 0x00;
                WiiExtension_extensionMemory[0xFA+4] = 0x01;
                WiiExtension_extensionMemory[0xFA+5] = 0x12;
                extensionController = new UDrawExtension();
                break;
            case WII_EXTENSION_MOTION_PLUS:
                WiiExtension_extensionMemory[0xFA+0] = 0x00;
                WiiExtension_extensionMemory[0xFA+4] = 0x04;
                WiiExtension_extensionMemory[0xFA+5] = 0x05;
                extensionController = new MotionPlusExtension();
                break;
        }

#if WII_EXTENSION_DEBUG==true
        //printf("%02x%02x %02x%02x %02x%02x = %d (%d)\n", WiiExtension_extensionMemory[0xFA+0], WiiExtension_extensionMemory[0xFA+1], WiiExtension_extensionMemory[0xFA+2], WiiExtension_extensionMemory[0xFA+3], WiiExtension_extensionMemory[0xFA+4], WiiExtension_extensionMemory[0xFA+5], extensionType, (extensionController == NULL));
#endif

        isReady = true;
    } else {
        uint8_t idRead[32];
        uint8_t regWrite[16];
        bool canContinue = true;
        int8_t result, retryCtr;

        // we need to determine which address gets used
        if (isMotionPlus && !isExtension) {
            // Motion Plus only
#if WII_EXTENSION_DEBUG==true
            printf("WiiExtension::start Motion Plus detected\n");
#endif
            address = WII_MOTIONPLUS_I2C_ADDR;
        } else if (!isMotionPlus && isExtension) {
            // Extension only
#if WII_EXTENSION_DEBUG==true
            printf("WiiExtension::start Extension detected\n");
#endif
            address = WII_EXTENSION_I2C_ADDR;
        } else if (isMotionPlus && isExtension) {
            // Both attached
#if WII_EXTENSION_DEBUG==true
            printf("WiiExtension::start Motion Plus & Extension detected\n");
#endif
            address = WII_MOTIONPLUS_I2C_ADDR;
        } else {
            // Nothing attached
#if WII_EXTENSION_DEBUG==true
            printf("WiiExtension::start No Extension devices detected\n");
#endif
            canContinue = false;
            isReady = false;
            return;
        }

        if (!isReady) {
#if WII_EXTENSION_ENCRYPTION==false
            if (canContinue) {
                regWrite[0] = 0xF0;
                regWrite[1] = 0x55;
                result = doI2CWrite(regWrite, 2);
                canContinue = (result > -1);

#if WII_EXTENSION_DEBUG==true
                printf("WiiExtension::start 0xF0? %1d\n", result);
#endif
            } else {
                canContinue = false;
            }
            result = doI2CRead(regWrite, 1);

            if (canContinue) {
                regWrite[0] = 0xFB;
                regWrite[1] = 0x00;
                result = doI2CWrite(regWrite, 2);
                canContinue = (result > -1);

#if WII_EXTENSION_DEBUG==true
                printf("WiiExtension::start 0xFB? %1d\n", result);
#endif
            } else {
                canContinue = false;
            }
#endif

            result = doI2CRead(regWrite, 1);

            if (canContinue) {
                for (retryCtr = 0; retryCtr < 3; retryCtr++) {
                    // set data format
                    regWrite[0] = 0xFE;
                    if (isMotionPlus) {
                        //regWrite[1] = 0x04;
                        regWrite[1] = 0x05;
                    } else {
                        regWrite[1] = 0x03;
                    }
                    result = doI2CWrite(regWrite, 2);

#if WII_EXTENSION_DEBUG==true
                    printf("WiiExtension::start 0xFE? %1d\n", result);
#endif

                    // no error returned, check the device ID
                    if (result > -1) {
                        if (isMotionPlus) {
                            // switch address back
                            address = WII_EXTENSION_I2C_ADDR;
                        }
                        
                        result = doI2CRead(idRead, 2);
                        isReady = true;

#if WII_EXTENSION_DEBUG==true
                        printf("WiiExtension::start 0xFE Check %02x %02x\n", idRead[0], idRead[1]);
#endif
                        break;
                    }
                }
            }

            result = doI2CRead(regWrite, 1);
        }

#if WII_EXTENSION_DEBUG==true
        printf("WiiExtension::start Is Ready? %01d\n", isReady);
#endif

        if (isReady) {
            // fetch the extension ID
            regWrite[0] = 0xFA;
            result = doI2CWrite(&regWrite[0], 1);

#if WII_EXTENSION_DEBUG==true
            printf("WiiExtension::start Extension Check? %1d\n", result);
#endif

            extensionType = WII_EXTENSION_NONE;

            // continue if the write was successful
            if (result > -1) {
                result = doI2CRead(idRead, 6);

                if (idRead[2] != 0xA4 || idRead[3] != 0x20) return;

                if (idRead[5] == 0x00) {
                    extensionType = WII_EXTENSION_NUNCHUCK;
                    extensionController = new NunchuckExtension();
                } else if (idRead[5] == 0x01) {
                    extensionType = WII_EXTENSION_CLASSIC;
                    //if (idRead[0] == 0x01) {
                    //    extensionType = WII_EXTENSION_CLASSIC_PRO;
                    //}
                    extensionController = new ClassicExtension();
                } else if (idRead[5] == 0x03) {
                    extensionType = WII_EXTENSION_GUITAR;
                    if (idRead[0] == 0x01) {
                        extensionType = WII_EXTENSION_DRUMS;
                        extensionController = new DrumExtension();
                    } else if (idRead[0] == 0x03) {
                        extensionType = WII_EXTENSION_TURNTABLE;
                        extensionController = new TurntableExtension();
                    } else {
                        extensionController = new GuitarExtension();
                    }
                } else if (idRead[5] == 0x05) {
                    extensionType = WII_EXTENSION_MOTION_PLUS;
                    extensionController = new MotionPlusExtension();
                } else if (idRead[5] == 0x11) {
                    extensionType = WII_EXTENSION_TAIKO;
                    extensionController = new TaikoExtension();
                } else if (idRead[5] == 0x12) {
                    extensionType = WII_EXTENSION_UDRAW;
                    extensionController = new UDrawExtension();
                }

                // in certain situations (eg. Nunchuck), setting the data type in reset() does not affect what this value will be
                dataType = idRead[4];
                if (dataType == WII_DATA_TYPE_0) dataType = WII_DATA_TYPE_1;
                extensionController->init(dataType);
                extensionController->setExtensionType(extensionType);

#if WII_EXTENSION_DEBUG==true
                printf("WiiExtension::Extension ID: %02x%02x %02x%02x %02x%02x\n", idRead[0], idRead[1], idRead[2], idRead[3], idRead[4], idRead[5]);
                printf("WiiExtension::Data Format: %02x\n",idRead[4]);
#endif

                if (extensionType != WII_EXTENSION_NONE) {
#if WII_EXTENSION_CALIBRATION==true
#if WII_EXTENSION_DEBUG==true
                    printf("WiiExtension::Calibration Data\n");
#endif
                    regWrite[0] = 0x20;
                    doI2CWrite(regWrite, 1);

                    doI2CRead(idRead, 32);
                    extensionController->calibrate(idRead);
#endif
                } else {
#if WII_EXTENSION_DEBUG==true
                    printf("WiiExtension::Unknown Extension: %02x%02x %02x%02x %02x%02x\n", idRead[0], idRead[1], idRead[2], idRead[3], idRead[4], idRead[5]);
#endif
                }

                regWrite[0] = 0x00;
                result = doI2CWrite(regWrite, 1);
            }
        }
    }
}


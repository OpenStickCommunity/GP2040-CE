int WiiExtension::doI2CWrite(uint8_t *pData, int iLen) {
    int result = i2c->write(address, pData, iLen, false);
    waitUntil_us(WII_EXTENSION_DELAY);
    return result;
}

int WiiExtension::doI2CRead(uint8_t *pData, int iLen) {
    int result = i2c->read(address, pData, iLen, false);
    waitUntil_us(WII_EXTENSION_DELAY);
#if WII_EXTENSION_ENCRYPTION==true
    for (int i = 0; i < iLen; ++i) {
        pData[i] = WII_DECRYPT_BYTE(pData[i]);
    }
#endif
    return result;
}

uint8_t WiiExtension::doI2CTest() {
    int result;
    uint8_t rxdata;
    result = doI2CRead(&rxdata, 1);
#if WII_EXTENSION_DEBUG==true
    printf("WiiExtension::doI2CTest: %d\n", result);
#endif
    return (result >= 0);
}

void WiiExtension::doI2CInit() {
    isMotionPlus = false;
    isExtension = false;

    bool extensionCheck = i2c->test(WII_EXTENSION_I2C_ADDR);
    waitUntil_us(WII_EXTENSION_DELAY);

    bool motionPlusCheck = i2c->test(WII_MOTIONPLUS_I2C_ADDR);
    waitUntil_us(WII_EXTENSION_DELAY);

    // since init is unused, let's use this to detect normal or MotionPlus mode
    if (extensionCheck) isExtension = true;
    if (motionPlusCheck) isMotionPlus = true;
}

void WiiExtension::waitUntil_us(uint64_t us) {
    WiiExtension_alarmFired = false;

    // Enable the interrupt for our alarm (the timer outputs 4 alarm irqs)
    hw_set_bits(&timer_hw->inte, 1u << WII_ALARM_NUM);
    // Set irq handler for alarm irq
    irq_set_exclusive_handler(WII_ALARM_IRQ, alarmIRQ);
    // Enable the alarm irq
    irq_set_enabled(WII_ALARM_IRQ, true);
    // Enable interrupt in block and at processor

    // Alarm is only 32 bits so if trying to delay more
    // than that need to be careful and keep track of the upper
    // bits
    uint64_t target = timer_hw->timerawl + us;

    // Write the lower 32 bits of the target time to the alarm which
    // will arm it
    timer_hw->alarm[WII_ALARM_NUM] = (uint32_t) target;

    while (!WiiExtension_alarmFired);
}

void WiiExtension::alarmIRQ() {
    // Clear the alarm irq
    hw_clear_bits(&timer_hw->intr, 1u << WII_ALARM_NUM);

    // Assume alarm 0 has fired
    WiiExtension_alarmFired = true;
}

void WiiExtension::i2cHandler(i2c_inst_t *i2c, i2c_slave_event_t event) {
    switch (event) {
        case I2C_SLAVE_RECEIVE: {
                // Master sent us a byte. If this is the first byte of the
                // transaction, it's the register address to seek to.
                // Subsequent bytes are writes to that address with
                // auto-increment.
                uint8_t b = (uint8_t)i2c_read_byte_raw(i2c);
                if (!WiiExtension_hasAddress) {
                    WiiExtension_memoryCursor = b;
                    WiiExtension_hasAddress = true;
                    // Suppress the very common 0x00 data-poll seek so init reads
                    // (0x20, 0x30, 0xFA, 0xFE, ...) are visible in the log.
                    if (b != 0x00) wii_log_push('A', b, 0);
                } else {
                    // Decrypt incoming write IF encryption is on � but skip
                    // the decrypt for control registers (0xF0, 0xFB, 0xFE),
                    // which the Wii always sends in plaintext for protocol
                    // negotiation. This lets a new session reset stale
                    // encryption state from a previous session: when 0x55
                    // arrives at 0xF0, we recognize it raw and disable.
                    bool is_control_reg = ((WiiExtension_memoryCursor == 0xF0) || (WiiExtension_memoryCursor == 0xFB));
                    uint8_t store_b = b;
                    if (WiiExtension_isCryptoEnabled && !is_control_reg) {
                        decrypt(&store_b, (uint8_t)WiiExtension_memoryCursor, 1);
                    }
                    if (WiiExtension_memoryCursor < WII_EXT_MEMORY_SIZE) {
                        WiiExtension_extensionMemory[WiiExtension_memoryCursor] = store_b;
                    }
                    wii_log_push('W', (uint8_t)WiiExtension_memoryCursor, store_b);
                    // Encryption negotiation. Both 0x55 and 0xAA writes to 0xF0
                    // disable encryption � the Wiimote then sends a fresh
                    // 16-byte key in plaintext to 0x40-0x4F, and we re-init
                    // the cipher when the last key byte (0x4F) lands.
                    if (WiiExtension_memoryCursor == 0xF0) {
                        if (store_b == 0x55 || store_b == 0xAA) {
                            WiiExtension_isCryptoEnabled = false;
                        }
                    } else if (WiiExtension_memoryCursor == 0x4F && WiiExtension_extensionMemory[0xF0] == 0xAA) {
                        initCrypt(&WiiExtension_extensionMemory[0x40]);
                    } else if (WiiExtension_memoryCursor == 0xFE) {
                        // Format-mode switch — mirror into id[4] (the ID byte
                        // and the 0xFE format register must agree per real-CC
                        // behavior). Also seed sensible neutral data bytes
                        // for the new format so a host read between the mode
                        // switch and the next BT input event sees centred
                        // sticks instead of stale bytes from the old format.
                        uint8_t prev = WiiExtension_extensionMemory[0xFE];
                        if (prev != store_b) {
                            wii_log_push('M', prev, store_b);  // 'M' = mode change
                        }
                        WiiExtension_extensionMemory[0xFE] = store_b;
                        WiiExtension_extensionMemory[0xFA + 4] = store_b;
                    }
                    WiiExtension_memoryCursor = (uint16_t)(WiiExtension_memoryCursor + 1) % WII_EXT_MEMORY_SIZE;
                }
            }
            break;
        case I2C_SLAVE_REQUEST: {
                // Master wants to read. Encrypt if encryption is active.
                uint8_t out_byte;
                if (WiiExtension_memoryCursor < WII_EXT_MEMORY_SIZE) {
                    out_byte = WiiExtension_extensionMemory[WiiExtension_memoryCursor];
                    if (WiiExtension_isCryptoEnabled) {
                        encrypt(&out_byte, (uint8_t)WiiExtension_memoryCursor, 1);
                    }
                } else {
                    out_byte = 0xFF;
                }
                // Log the first byte of each read transaction (skip 0x00 polls).
                if (WiiExtension_firstRead && WiiExtension_memoryCursor != 0x00) {
                    wii_log_push('R', (uint8_t)WiiExtension_memoryCursor, out_byte);
                }
                WiiExtension_firstRead = false;
                i2c_write_byte_raw(i2c, out_byte);
                WiiExtension_memoryCursor = (uint16_t)(WiiExtension_memoryCursor + 1) % WII_EXT_MEMORY_SIZE;
            }
            break;
        case I2C_SLAVE_FINISH: {
                // STOP or RESTART: end of this transaction. Reset the
                // "next-byte-is-address" flag so the next RECEIVE starts a
                // fresh seek. Keep `memoryCursor` pointing where the last read
                // left off � real Wii extensions auto-increment across
                // back-to-back reads, and some games depend on it.
                WiiExtension_hasAddress = false;
                WiiExtension_firstRead = true;
            }
            break;
        default: {
            break;
        }
    }
}

static inline uint8_t ror8(uint8_t a, uint8_t b)
{
    return (a>>b) | ((a<<(8-b))&0xff);
}

void WiiExtension::initCrypt(const uint8_t key[16]) {
    int idx, i;
    const uint8_t * ans;
    uint8_t t0[10];

    //determine idx with simple brute force
    for (idx = 0; idx < 7; idx++)
    {
        ans = ans_tbl[idx];

        for(i = 0; i < 10; i++)
        {
            t0[i] = sboxes[0][key[9 - i]];
        }

        //todo: clean up this nasty mess
        if ((key[0xf] == (uint8_t)((ror8(ans[0]^t0[5],t0[2]%8) - t0[9]) ^ t0[4])) &&
        (key[0xe] == (uint8_t)((ror8(ans[1]^t0[1],t0[0]%8) - t0[5]) ^ t0[7])) &&
        (key[0xd] == (uint8_t)((ror8(ans[2]^t0[6],t0[8]%8) - t0[2]) ^ t0[0])) &&
        (key[0xc] == (uint8_t)((ror8(ans[3]^t0[4],t0[7]%8) - t0[3]) ^ t0[2])) &&
        (key[0xb] == (uint8_t)((ror8(ans[4]^t0[1],t0[6]%8) - t0[3]) ^ t0[4])) &&
        (key[0xa] == (uint8_t)((ror8(ans[5]^t0[7],t0[8]%8) - t0[5]) ^ t0[9])))
        {
            break;
        }
    }

    WiiExtension_ft[0] = sboxes[idx+1][key[0xb]] ^ sboxes[idx+2][key[0x6]];
    WiiExtension_ft[1] = sboxes[idx+1][key[0xd]] ^ sboxes[idx+2][key[0x4]];
    WiiExtension_ft[2] = sboxes[idx+1][key[0xa]] ^ sboxes[idx+2][key[0x2]];
    WiiExtension_ft[3] = sboxes[idx+1][key[0xf]] ^ sboxes[idx+2][key[0x7]];
    WiiExtension_ft[4] = sboxes[idx+1][key[0xe]] ^ sboxes[idx+2][key[0x5]];
    WiiExtension_ft[5] = sboxes[idx+1][key[0xc]] ^ sboxes[idx+2][key[0x0]];
    WiiExtension_ft[6] = sboxes[idx+1][key[0x9]] ^ sboxes[idx+2][key[0x3]];
    WiiExtension_ft[7] = sboxes[idx+1][key[0x8]] ^ sboxes[idx+2][key[0x1]];

    WiiExtension_sb[0] = sboxes[idx+1][key[0xf]] ^ sboxes[idx+2][key[0x8]];
    WiiExtension_sb[1] = sboxes[idx+1][key[0xa]] ^ sboxes[idx+2][key[0x5]];
    WiiExtension_sb[2] = sboxes[idx+1][key[0xc]] ^ sboxes[idx+2][key[0x9]];
    WiiExtension_sb[3] = sboxes[idx+1][key[0xd]] ^ sboxes[idx+2][key[0x0]];
    WiiExtension_sb[4] = sboxes[idx+1][key[0xb]] ^ sboxes[idx+2][key[0x2]];
    WiiExtension_sb[5] = sboxes[idx+1][key[0xe]] ^ sboxes[idx+2][key[0x1]];
    WiiExtension_sb[6] = sboxes[idx+1][key[0x6]] ^ sboxes[idx+2][key[0x4]];
    WiiExtension_sb[7] = sboxes[idx+1][key[0x7]] ^ sboxes[idx+2][key[0x3]];

    WiiExtension_isCryptoEnabled = true;
#if WII_EXTENSION_DEBUG==true
    //printf("[wii_ext_crypto] Encryption enabled (ft=%02X%02X%02X%02X sb=%02X%02X%02X%02X)\n", WiiExtension_ft[0], WiiExtension_ft[1], WiiExtension_ft[2], WiiExtension_ft[3], WiiExtension_sb[0], WiiExtension_sb[1], WiiExtension_sb[2], WiiExtension_sb[3]);
#endif
}

void WiiExtension::encrypt(uint8_t* data, uint8_t addr, uint8_t len) {
    for (int i = 0; i < len; i++)
    {
        data[i] = (data[i] - WiiExtension_ft[(i + addr) % 8]) ^ WiiExtension_sb[(i + addr) % 8];
    }
}

void WiiExtension::decrypt(uint8_t* data, uint8_t addr, uint8_t len) {
    for (int i = 0; i < len; i++)
    {
        data[i] = (data[i] ^ WiiExtension_sb[(i + addr) % 8]) + WiiExtension_ft[(i + addr) % 8];
    }
}